"""Evaluation run summary/report helpers."""

from __future__ import annotations

import json
from collections import Counter
from collections.abc import Mapping
from pathlib import Path
from typing import Any, TypeGuard

from omnidreams.eval.validation import validate_generated_run


def build_run_summary(run_root: Path) -> dict[str, Any]:
    """Build a machine-readable summary for an OmniDreams evaluation run."""

    generated = _summarize_generated(run_root)
    validation = _summarize_validation(run_root)
    drivinggen = _summarize_drivinggen(run_root)
    worldlens = _summarize_worldlens(run_root)
    return {
        "kind": "omnidreams_evaluation_summary",
        "run_root": str(run_root),
        "generated": generated,
        "validation": validation,
        "drivinggen": drivinggen,
        "worldlens": worldlens,
        "notes": [
            "DrivingGen FVD-lite is useful as a fixed-split regression metric; lower is better on the same split.",
            "DrivingGen reference-vs-reference FVD is diagnostic only and measures split diversity, not model quality.",
            "WorldLens consistency metrics are roughly higher-is-better, with 1.0 as the idealized upper bound.",
            "WorldLens top-level metric_results.json may contain null values; use per-metric JSON artifacts for scores.",
        ],
    }


def write_run_summary_json(summary: dict[str, Any], output: Path) -> None:
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(
        json.dumps(summary, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )


def write_run_summary_markdown(summary: dict[str, Any], output: Path) -> None:
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(render_run_summary_markdown(summary), encoding="utf-8")


def render_run_summary_markdown(summary: dict[str, Any]) -> str:
    lines = [
        "# OmniDreams Evaluation Summary",
        "",
        f"- Run root: `{summary['run_root']}`",
    ]

    generated = summary["generated"]
    lines.extend(
        [
            "",
            "## Generated Clips",
            "",
            f"- Case directories: {generated['case_directories']}",
            f"- Generated MP4s: {generated['generated_mp4_count']}",
            f"- Generated bytes: {generated['generated_mp4_total_bytes']}",
        ]
    )

    validation = summary["validation"]
    lines.extend(
        [
            "",
            "## Validation",
            "",
            f"- Cases checked: {validation['case_count']}",
            f"- Failures: {validation['failure_count']}",
            f"- Frame counts: {_format_counter(validation['runner_written_frames'])}",
            f"- Total blocks: {_format_counter(validation['total_blocks'])}",
            f"- HD-map frames: {_format_counter(validation['hdmap_frames'])}",
        ]
    )
    if validation["failed_cases"]:
        lines.append("- Failed cases:")
        for case in validation["failed_cases"]:
            lines.append(f"  - `{case['uuid']}`: {'; '.join(case['issues'])}")

    drivinggen = summary["drivinggen"]
    lines.extend(["", "## DrivingGen", ""])
    if not drivinggen["fvd_lite"]:
        lines.append("- No DrivingGen FVD-lite results found.")
    else:
        lines.append("| Split | Model | Exp | FVD | Path |")
        lines.append("| --- | --- | --- | ---: | --- |")
        for result in drivinggen["fvd_lite"]:
            lines.append(
                "| "
                f"{result.get('split', '')} | "
                f"{result.get('model_name', '')} | "
                f"{result.get('exp_id', '')} | "
                f"{_format_number(result.get('value'))} | "
                f"`{result.get('path', '')}` |"
            )
    if drivinggen["reference_diagnostics"]:
        lines.extend(["", "Diagnostic reference-vs-reference FVD:"])
        lines.append("| Split A | Split B | FVD | Path |")
        lines.append("| --- | --- | ---: | --- |")
        for result in drivinggen["reference_diagnostics"]:
            lines.append(
                "| "
                f"{result.get('split_a', '')} | "
                f"{result.get('split_b', '')} | "
                f"{_format_number(result.get('value'))} | "
                f"`{result.get('path', '')}` |"
            )

    worldlens = summary["worldlens"]
    lines.extend(["", "## WorldLens", ""])
    stage = worldlens.get("stage_manifest")
    if stage:
        lines.extend(
            [
                f"- Staged cases: {stage['case_count']}",
                f"- Generated frame counts: {stage['generated_frame_counts']}",
                f"- Reference frame counts: {stage['reference_frame_counts']}",
                f"- Temporal policy: {', '.join(stage['temporal_policy'])}",
            ]
        )
    if not worldlens["runs"]:
        lines.append("- No WorldLens summary JSONs found.")
    for run in worldlens["runs"]:
        lines.extend(
            [
                "",
                f"WorldLens split `{run['split']}`:",
                "",
                "| Artifact | Count | Consistency | TJI | TS | TS Range |",
                "| --- | ---: | ---: | ---: | ---: | --- |",
            ]
        )
        for metric in run["artifact_metrics"]:
            lines.append(
                "| "
                f"{metric['artifact']} | "
                f"{metric['video_count']} | "
                f"{_format_number(metric.get('temporal_consistency_per_frame'))} | "
                f"{_format_number(metric.get('tji_per_frame'))} | "
                f"{_format_number(metric.get('ts_per_frame'))} | "
                f"{_format_number(metric.get('ts_min'))}..{_format_number(metric.get('ts_max'))} |"
            )

    lines.append("")
    return "\n".join(lines)


def _summarize_generated(run_root: Path) -> dict[str, Any]:
    generated_root = run_root / "generated"
    case_dirs = (
        sorted(path for path in generated_root.iterdir() if path.is_dir())
        if generated_root.exists()
        else []
    )
    mp4s = [
        path / "generated.mp4"
        for path in case_dirs
        if (path / "generated.mp4").exists()
    ]
    return {
        "root": str(generated_root),
        "case_directories": len(case_dirs),
        "generated_mp4_count": len(mp4s),
        "generated_mp4_total_bytes": sum(path.stat().st_size for path in mp4s),
    }


def _summarize_validation(run_root: Path) -> dict[str, Any]:
    generated_root = run_root / "generated"
    if generated_root.exists():
        results = [result.to_dict() for result in validate_generated_run(run_root)]
        source = "computed"
    else:
        validation_path = _latest_path(run_root.glob("validation*.json"))
        results = _read_json(validation_path) if validation_path is not None else []
        source = str(validation_path) if validation_path is not None else None
    if not isinstance(results, list):
        results = []

    failed_cases = [
        {"uuid": result.get("uuid"), "issues": result.get("issues", [])}
        for result in results
        if not result.get("ok", False)
    ]
    return {
        "source": source,
        "case_count": len(results),
        "ok_count": sum(1 for result in results if result.get("ok", False)),
        "failure_count": len(failed_cases),
        "failed_cases": failed_cases,
        "runner_written_frames": _counter_dict(
            result.get("runner_written_frames") for result in results
        ),
        "expected_frames_from_steps": _counter_dict(
            result.get("expected_frames_from_steps") for result in results
        ),
        "total_blocks": _counter_dict(result.get("total_blocks") for result in results),
        "hdmap_frames": _counter_dict(result.get("hdmap_frames") for result in results),
    }


def _summarize_drivinggen(run_root: Path) -> dict[str, Any]:
    log_root = run_root / "evaluators" / "DrivingGen" / "cache" / "eval_logs"
    fvd_lite: list[dict[str, Any]] = []
    reference_diagnostics: list[dict[str, Any]] = []
    if log_root.exists():
        for path in sorted(log_root.rglob("*.json")):
            payload = _read_json(path)
            if not isinstance(payload, dict):
                continue
            row = {
                "path": str(path),
                "metric": payload.get("metric"),
                "value": payload.get("value"),
                "results": payload.get("results"),
            }
            if "split" in payload:
                row.update(
                    {
                        "split": payload.get("split"),
                        "model_name": payload.get("model_name"),
                        "exp_id": payload.get("exp_id"),
                    }
                )
                fvd_lite.append(row)
            elif "split_a" in payload and "split_b" in payload:
                row.update(
                    {
                        "split_a": payload.get("split_a"),
                        "split_b": payload.get("split_b"),
                    }
                )
                reference_diagnostics.append(row)
    return {
        "fvd_lite": fvd_lite,
        "reference_diagnostics": reference_diagnostics,
    }


def _summarize_worldlens(run_root: Path) -> dict[str, Any]:
    worldlens_root = run_root / "evaluators" / "WorldLens"
    stage_manifest = _summarize_worldlens_stage_manifest(
        worldlens_root / "generated_results" / "omnidreams" / "stage_manifest.json"
    )
    runs: list[dict[str, Any]] = []
    eval_log_root = worldlens_root / "cache" / "eval_logs"
    if eval_log_root.exists():
        for path in sorted(eval_log_root.rglob("*-worldlens.json")):
            payload = _read_json(path)
            if not isinstance(payload, dict):
                continue
            runs.append(
                {
                    "split": path.parent.name,
                    "path": str(path),
                    "method_name": payload.get("method_name"),
                    "config_name": payload.get("config_name"),
                    "metric_results_path": payload.get("metric_results_path"),
                    "artifact_metrics": _worldlens_artifact_metrics(
                        payload.get("artifact_results", {})
                    ),
                }
            )
    return {
        "stage_manifest": stage_manifest,
        "runs": runs,
    }


def _summarize_worldlens_stage_manifest(path: Path) -> dict[str, Any] | None:
    payload = _read_json(path)
    if not isinstance(payload, dict):
        return None
    cases = payload.get("cases")
    if not isinstance(cases, list):
        cases = []
    mismatches = [
        case.get("uuid")
        for case in cases
        if isinstance(case, dict)
        and case.get("generated_frame_count") != case.get("reference_frame_count")
    ]
    return {
        "path": str(path),
        "case_count": len(cases),
        "generated_frame_counts": sorted(
            {
                case.get("generated_frame_count")
                for case in cases
                if isinstance(case, dict)
                and case.get("generated_frame_count") is not None
            }
        ),
        "reference_frame_counts": sorted(
            {
                case.get("reference_frame_count")
                for case in cases
                if isinstance(case, dict)
                and case.get("reference_frame_count") is not None
            }
        ),
        "temporal_policy": sorted(
            {
                case.get("temporal_policy")
                for case in cases
                if isinstance(case, dict) and case.get("temporal_policy")
            }
        ),
        "frame_count_mismatch_count": len(mismatches),
        "frame_count_mismatches": mismatches,
    }


def _worldlens_artifact_metrics(artifact_results: object) -> list[dict[str, Any]]:
    if not isinstance(artifact_results, Mapping):
        return []
    rows: list[dict[str, Any]] = []
    for artifact, result in sorted(
        artifact_results.items(), key=lambda item: str(item[0])
    ):
        if not isinstance(result, Mapping):
            continue
        result_map = {str(key): value for key, value in result.items()}
        video_results = result_map.get("video_results", [])
        if not isinstance(video_results, list):
            video_results = []
        ts_values: list[int | float] = []
        for item in video_results:
            if not isinstance(item, Mapping):
                continue
            item_map = {str(key): value for key, value in item.items()}
            ts = item_map.get("ts")
            if _is_number(ts):
                ts_values.append(ts)
        rows.append(
            {
                "artifact": str(artifact),
                "method_name": result_map.get("method_name"),
                "repeat": result_map.get("repeat"),
                "video_count": len(video_results),
                "temporal_consistency_per_frame": result_map.get(
                    "temporal_consistency_per_frame"
                ),
                "tji_per_frame": result_map.get("tji_per_frame"),
                "ts_per_frame": result_map.get("ts_per_frame"),
                "ts_min": min(ts_values) if ts_values else None,
                "ts_max": max(ts_values) if ts_values else None,
            }
        )
    return rows


def _counter_dict(values) -> dict[str, int]:
    return {
        str(key): count
        for key, count in sorted(
            Counter(value for value in values if value is not None).items()
        )
    }


def _format_counter(value: object) -> str:
    if not isinstance(value, dict) or not value:
        return "none"
    return ", ".join(f"{key} x {count}" for key, count in value.items())


def _format_number(value: object) -> str:
    if not _is_number(value):
        return ""
    return f"{float(value):.6f}"


def _is_number(value: object) -> TypeGuard[int | float]:
    return isinstance(value, (int, float)) and not isinstance(value, bool)


def _latest_path(paths) -> Path | None:
    candidates = [path for path in paths if path.is_file()]
    if not candidates:
        return None
    return max(candidates, key=lambda path: path.stat().st_mtime)


def _read_json(path: Path | None) -> Any:
    if path is None or not path.exists():
        return None
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return None
